package uk.ac.yorksj.year2.sem1.assignment1;

public class paddle{
	
	private int width;
	private int height;
	
	
	public paddle() {
		
	}
	
	public paddle(int x, int y) {
		this.width = x;
		this.height = y;
	}
	
	public int getWidth() {
		return this.width;
	}
	
	public int getHeight() {
		return this.height;
	}
	

}
